radius = 42
pi = 3.1415926
# Площадь круга
print(round(radius * pi ** 2, 4))

point_1 = (23, 34)

def f(p):
    # Проверяем, находится ли точка внутри круга
    print((p[0] ** 2 + p[1] ** 2) ** 0.5 <= radius)

# Проверка для первой точки
f(point_1)

point_2 = (30, 30)

# Проверка для второй точки
f(point_2)
